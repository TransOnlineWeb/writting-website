    export default {
        data(){
            return{
                orders: {},
                form: new Form({
                   status: ''
                })
            }
        },
        methods:{
          editModal(order){
              $('#addnew').modal('show');
              this.form.fill(order);
            },
            getOrders(){
              axios.get("/api/task").then(({ data }) => ([this.orders = data]));
            },
        },
        created() {
            this.getOrders();
        }
    }
</script>
